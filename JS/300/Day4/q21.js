/** JS built-in error type */

/**
 * EvalError
 * : an error that occurs regarding the global function
 * 
 * RangeError
 * : an error that occurs when a numeric variable or parameter is outside its valid range
 * 
 * ReferenceError
 * : an error that occurs when dereferencing an invalid reference
 * 
 * SyntaxError
 * : a syntax error
 * 
 * TypeError
 * : occurs when a variable or parameter is not a valid tpe
 * 
 * URLError
 * : occurs when `encodeURL()` or `decodeURL()` are passed invalid parameters
 * 
 * AggregateError
 * : representing several errors wrapped in a single error when multiple errors need to be reported by an operation, for ex) Promise.any()
 * 
 * InternalError
 * : when an internal error in the JS engine is thrown. "too much recursion"
 */